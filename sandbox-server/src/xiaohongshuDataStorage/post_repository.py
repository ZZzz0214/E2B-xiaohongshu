    def batch_insert_posts(self, posts_list: List[Dict[str, Any]]) -> Dict[str, int]:
        """ÊâπÈáèÊèíÂÖ•Â∏ñÂ≠ê"""
        success_count = 0
        error_count = 0
        
        logger.info(f"ÂºÄÂßãÊâπÈáèÊèíÂÖ• {len(posts_list)} ‰∏™Â∏ñÂ≠ê")
        
        for post_data in posts_list:
            try:
                if self.insert_post(post_data):
                    success_count += 1
                else:
                    error_count += 1
            except Exception as e:
                error_count += 1
                logger.error(f"ÊâπÈáèÊèíÂÖ•Â∏ñÂ≠êÂ§±Ë¥•: {e}")
        
        result = {
            'total': len(posts_list),
            'success': success_count,
            'error': error_count
        }
        
        logger.info(f"ÊâπÈáèÊèíÂÖ•ÂÆåÊàê: {result}")
        return result
    
