    def parse_publish_time(self, time_raw: str) -> Optional[str]:
        """Ëß£ÊûêÂèëÂ∏ÉÊó∂Èó¥Â≠óÁ¨¶‰∏≤‰∏∫DATEÊ†ºÂºè"""
        if not time_raw:
            return None
            
        try:
            now = datetime.now()
            
            # Â§ÑÁêÜÁõ∏ÂØπÊó∂Èó¥
            if "Â§©Ââç" in time_raw:
                days = int(re.search(r'(\d+)Â§©Ââç', time_raw).group(1))
                target_date = now - timedelta(days=days)
                return target_date.strftime('%Y-%m-%d')
            
            elif "Êò®Â§©" in time_raw:
                target_date = now - timedelta(days=1)
                return target_date.strftime('%Y-%m-%d')
            
            elif "‰ªäÂ§©" in time_raw or "ÂàöÂàö" in time_raw or "ÂàÜÈíüÂâç" in time_raw or "Â∞èÊó∂Ââç" in time_raw:
                return now.strftime('%Y-%m-%d')
            
            # Â§ÑÁêÜÂÖ∑‰ΩìÊó•Êúü Â¶Ç "08-20", "02-16"
            elif re.match(r'\d{2}-\d{2}', time_raw):
                month, day = time_raw.split('-')
                # ÂÅáËÆæÊòØÂΩìÂπ¥ÁöÑÊó•Êúü
                year = now.year
                target_date = datetime(year, int(month), int(day))
                
                # Â¶ÇÊûúÊó•ÊúüÂú®Êú™Êù•ÔºåÂàôËÆ§‰∏∫ÊòØÂéªÂπ¥ÁöÑ
                if target_date > now:
                    target_date = datetime(year - 1, int(month), int(day))
                
                return target_date.strftime('%Y-%m-%d')
            
            # Â§ÑÁêÜÂÖ∂‰ªñÊ†ºÂºè
            else:
                logger.warning(f"Êó†Ê≥ïËß£ÊûêÊó∂Èó¥Ê†ºÂºè: {time_raw}")
                return None
                
        except Exception as e:
            logger.error(f"Êó∂Èó¥Ëß£ÊûêÂ§±Ë¥•: {time_raw}, ÈîôËØØ: {e}")
            return None
    
