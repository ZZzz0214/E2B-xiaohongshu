    def update_post_status(self, post_id: str, status_updates: Dict) -> bool:
        """Êõ¥Êñ∞Â∏ñÂ≠êÁä∂ÊÄÅ"""
        try:
            # ÊûÑÂª∫SETÂ≠êÂè•
            set_clauses = []
            values = []
            
            for field, value in status_updates.items():
                set_clauses.append(f"{field} = %s")
                values.append(value)
            
            if not set_clauses:
                return False
            
            # Ê∑ªÂä†post_idÂà∞valuesÊúÄÂêé
            values.append(post_id)
            
            sql = f"""
            UPDATE xiaohongshu_posts 
            SET {', '.join(set_clauses)}
            WHERE post_id = %s
            """
            
            db_manager.execute_insert(sql, tuple(values))
            logger.info(f"Â∏ñÂ≠êÁä∂ÊÄÅÊõ¥Êñ∞ÊàêÂäü: {post_id}, Êõ¥Êñ∞Â≠óÊÆµ: {list(status_updates.keys())}")
            return True
            
        except Exception as e:
            logger.error(f"Â∏ñÂ≠êÁä∂ÊÄÅÊõ¥Êñ∞Â§±Ë¥•: {post_id}, ÈîôËØØ: {e}")
            return False

